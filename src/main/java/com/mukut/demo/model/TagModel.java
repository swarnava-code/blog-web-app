package com.mukut.demo.model;

public class TagModel {
    private Boolean technology;
    private Boolean lifestyle;
    private Boolean motivation;
    private Boolean politics;
    private Boolean culture;

    public Boolean getTechnology() {
        return technology;
    }

    public void setTechnology(Boolean technology) {
        this.technology = technology;
    }

    public Boolean getLifestyle() {
        return lifestyle;
    }

    public void setLifestyle(Boolean lifestyle) {
        this.lifestyle = lifestyle;
    }

    public Boolean getMotivation() {
        return motivation;
    }

    public void setMotivation(Boolean motivation) {
        this.motivation = motivation;
    }

    public Boolean getPolitics() {
        return politics;
    }

    public void setPolitics(Boolean politics) {
        this.politics = politics;
    }

    public Boolean getCulture() {
        return culture;
    }

    public void setCulture(Boolean culture) {
        this.culture = culture;
    }
}
